const button = document.querySelector(".lang");
let lang = localStorage.getItem("zg-lang") || "zh";

function setLanguage(next) {
  lang = next;
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  button.textContent = lang === "zh" ? "中文" : "EN";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = copy[lang][node.dataset.i18n];
    if (value) node.textContent = value;
  });
  localStorage.setItem("zg-lang", lang);
}

button.addEventListener("click", () => setLanguage(lang === "zh" ? "en" : "zh"));
setLanguage(lang);
